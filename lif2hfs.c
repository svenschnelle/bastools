int main(int argc, char **argv)
{
	uint8_t *in, *out;
	size_t inlen;

	if (argc < 3) {
		fprintf(stderr, "%s: usage: %s <infile> <outfile>\n", argv[0], argv[0]);
		return 1;
	}

	in = mmap_file_read(argv[1], &inlen);
	if (in == MAP_FAILED)
		return 1;
	out = mmap_file_write(argv[2], inlen + 480);
	if (out == MAP_FAILED)
		return 1;
	memcpy(out, hfshdr, sizeof(hfshdr));
	memcpy(out + 0x100, in, 32); /* copy LIF header */
	memcpy(out + 0x200, in + 32, inlen - 32);
	msync(out, inlen + 480, MS_SYNC);
	munmap(out, inlen + 480);
	munmap(in, inlen);
	return 0;
}
